
public class instrumentdimen {
	
	public double altura;
	public double ancho;
	public String material;
	
	public instrumentdimen(double altura, double ancho, String material) {
		this.altura = altura;
		this.ancho = ancho;
		this.material = material;
		
	}
	public double getAltura() {
		return altura;
	}
	
	public double getAncho() {
		return ancho;
	
	}

	public String getMaterial() {
		return material;
	}
}
